    def test_dpr_dataset_json(self):
        dataset = DPRDataset(self.TRAIN2)
        self.assertEqual(dataset.samples[0]['question'],
                         'big little lies season 2 how many episodes')
        self.assertEqual(dataset.samples[0]['positive_ctxs'][0]['title'],
                         'Big Little Lies (TV series)')
        self.assertTrue(dataset.samples[0]['positive_ctxs'][0]['text']\
                        .startswith('series garnered several'))
        self.assertEqual(dataset.samples[0]['positive_ctxs'][1]['title'],
                         'Little People, Big World')
        self.assertTrue(dataset.samples[0]['positive_ctxs'][1]['text']\
                        .startswith('TLC announced a spin-off'))
