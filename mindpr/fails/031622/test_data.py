    TEST = '/common/home/jl2529/repositories/DPR/downloads/data/retriever/qas/nq-test.csv'

    def test_tensorize(self):
        dataset = DPRDataset(self.TRAIN2)
        tokenizer = BertTokenizer.from_pretrained('bert-base-uncased')
